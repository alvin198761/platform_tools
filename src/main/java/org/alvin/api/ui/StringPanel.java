    private String getStringReuslt(boolean listvar) {
        String result = "";
        String text = inputText.getText();
        String[] array = text.split("\r|\n|\r\n");
        for (String s : array) {
            if (s.trim().isEmpty()) {
                continue;
            }
            if (trimBox.isSelected()) {
                s = s.trim();
            } else if (replaceAllBox.isSelected()) {
                s = s.replaceAll("\\s|\t", "");
            }
            s = s.replaceAll("\"", "\\\"");
            if (!listvar) {
                result += "\"" + s + "\",";
            } else {
                String list = this.collectionNameText.getText().trim();
                s = "\"" + s + "\"";
                list = list + ".add(" + s + ");";
                result += list;
            }
            result += System.lineSeparator();
        }
        return result;
    }

    private String getObjectResult(boolean listvar) {
        String text = inputText.getText();
        String[] array = text.split("\r|\n|\r\n");
        String result = "";
        for (int i = 0; i < array.length; i++) {
            String l = array[i];
            l = l.replaceAll("\"", "\\\"");
            if (l.trim().isEmpty()) {
                continue;
            }
            String[] columns = l.trim().split("\t");
            String clazz = this.clazzNameText.getText().trim();
            String line = "";
            for (String s : columns) {
                if (trimBox.isSelected()) {
                    s = s.trim();
                } else if (replaceAllBox.isSelected()) {
                    s = s.replaceAll("\\s|\t", "");
                }
                line += ",\"" + s + "\"";
            }
            if (!listvar) {
                String rowText = clazz + " item" + i + " = new " + clazz + "({0})";
                String bean = MessageFormat.format(rowText, line.substring(1));
                result += bean + ";";
            } else {
                String rowText = "new " + clazz + "({0})";
                String bean = MessageFormat.format(rowText, line.substring(1));
                String list = this.collectionNameText.getText().trim();
                list = list + ".add(" + bean + ");";
                result += list;
            }
            result += System.lineSeparator();
        }
        return result;
    }
